public class Calculator {
    
    public int add(int x, int y) {
        return x + y;
    }

    public int add(int x, int y, int z) {
        return x + y + z;
    }

    public void add() {
        System.out.println("What to add?");
    }
 
    public double add(double x, double y) {
        return x + y;
    }

    public long add(long x, long y) {
        return x + y;
    }
}
